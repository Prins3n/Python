'''
Band Name Generator
'''
#input variabel som spør om plassen personen har vokst opp og lagrer den.
sted = input("Hva heter plassen du vokste opp? ")
#input variabel som ber om et kjæledyrnavn.
pet = input("Si et kjæledyr navn: ")
#Variabelen som lager bandnavnet gjennom å legge sammen stedet, et mellomrom og kjæledyret.
band_name = sted + " " + pet
#print statement som printer ut bandnavnet person skal få.
print(band_name)